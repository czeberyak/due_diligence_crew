from crewai import Task
from textwrap import dedent

class DueDiligenceCrewTasks:
    def safety_audit_task(self, agent, document_path) -> Task:
        return Task(
            description=dedent(f"""
                КРИТИЧЕСКИ ВАЖНО: Вы ОБЯЗАНЫ использовать инструменты `regulatory_search` и `semantic_search`.
                
                1. Прочитайте документ ({document_path}) через `document_search`.
                2. Определите тип проекта (химия, энергетика/СЭС/ВЭС, строительство и т.д.).
                3. На основе типа проекта выберите РЕЛЕВАНТНЫЕ категории нормативной базы:
                   - Для химии/нефтегаза: 02_industrial_safety, 04_pressure_equipment
                   - Для энергетики (СЭС, ВЭС, ТЭЦ): 08_energy
                   - Для строительства: 01_construction
                   - Универсальные (всегда нужны): 03_ecology, 06_labor, 07_fire_safety
                4. ИСПОЛЬЗУЙТЕ `regulatory_search` с параметром category для поиска в выбранных категориях.
                   Пример: regulatory_search(query='требования к подключению', category='08_energy')
                   Пример: regulatory_search(query='ПДК выбросов', category='03_ecology')
                5. НЕ ПРОСТО перечисляйте законы — НАЙДИТЕ конкретные пункты и сверьте с ТЭО.
                6. Выявите, чего в ТЭО НЕ ХВАТАЕТ согласно найденным законам.
                7. Выявите критические узкие места в системах защиты, автоматизации и утилизации отходов.
                
                ЗАПРЕЩЕНО писать "Полное соответствие" без цитирования конкретных пунктов законов.
                Если вы не используете regulatory_search и semantic_search, ваша работа будет считаться невыполненной.
            """),
            expected_output="Матрица рисков, где каждый риск подкреплен ВЫДЕРЖКОЙ из реального нормативного документа (с указанием конкретного пункта и категории).",
            agent=agent
        )

    def process_validation_task(self, agent, document_path):
        return Task(
            description=dedent(f"""
                КРИТИЧЕСКИ ВАЖНО: Вы ОБЯЗАНЫ использовать инструменты для проверки баланса.
                
                1. Используя `document_search`, найдите в {document_path} все таблицы баланса 
                   (используйте ключевые слова 'баланс', 'сырье', 'расход', 'выработка', 'мощность', 'КИУМ').
                2. Определите тип проекта:
                   - Если это ХИМИЧЕСКОЕ/ПРОИЗВОДСТВЕННОЕ ТЭО (есть сырье и продукты в тоннах/кг):
                     * Выпишите названия всех исходных компонентов и продуктов, а также их массы в кг.
                     * ОБЯЗАТЕЛЬНО передайте данные в `process_calculator` для расчета невязки.
                   - Если это ЭНЕРГЕТИЧЕСКОЕ ТЭО (СЭС, ВЭС, ТЭЦ — есть мощность в МВт и выработка в МВт·ч):
                     * Найдите: установленную мощность (МВт), заявленную выработку (МВт·ч/год), КИУМ (%).
                     * ОБЯЗАТЕЛЬНО передайте данные в `energy_balance_calculator` для проверки КИУМ.
                     * КРИТИЧЕСКАЯ ПРОВЕРКА: КИУМ для солнечных станций не может быть > 25%, для ветровых > 35%.
                3. Если невязка баланса > 5% ИЛИ КИУМ превышает физический максимум — 
                   вы ДОЛЖНЫ явно указать это как КРИТИЧЕСКУЮ ОШИБКУ ФИЗИЧЕСКОЙ НЕРЕАЛИЗУЕМОСТИ.
                4. ЗАПРЕЩЕНО придумывать цифры или писать, что у вас нет доступа.
                
                Если вы не используете process_calculator ИЛИ energy_balance_calculator, 
                ваша работа будет считаться невыполненной.
            """),
            expected_output="Точный расчет баланса (материального ИЛИ энергетического) на основе данных из PDF с указанием фактической невязки/КИУМ и вердиктом о реализуемости.",
            agent=agent
        )

    def financial_translation_task(self, agent, context_tasks) -> Task:
        return Task(
            description=dedent("""
                КРИТИЧЕСКИЕ ПРАВИЛА МАТЕМАТИКИ И ФИНАНСОВОГО АНАЛИЗА:
                
                1. ЗАПРЕЩЕНО выдумывать цифры "из головы" и ЗАПРЕЩЕНО писать "не определено".
                2. Если в ТЭО нет конкретных цифр (CAPEX, OPEX, штрафы), вы ДОЛЖНЫ:
                   - Оценить их на основе отраслевых аналогов и средних рыночных цен.
                   - Явно указать: "Оценка на основе отраслевых данных" или "Требуется уточнение у заказчика".
                   - Дать диапазон значений (минимум-максимум), а не "не определено".
                3. Если инженер-технолог сообщил, что выход продукта превышает 100% ИЛИ 
                   КИУМ превышает физический максимум — вы ДОЛЖНЫ указать это как 
                   КРИТИЧЕСКИЙ РИСК ФИЗИЧЕСКОЙ НЕРЕАЛИЗУЕМОСТИ с финансовым эффектом 100% потери инвестиций.
                4. Переведите каждый риск в финансовые показатели СТРОГО В РУБЛЯХ (если ТЭО в рублях).
                5. Рассчитайте:
                   - Базовый CAPEX и OPEX (из ТЭО или оценка)
                   - Дополнительный CAPEX на устранение рисков (оборудование, системы защиты)
                   - Дополнительный OPEX (обслуживание, штрафы, потери продукции)
                   - Итоговые показатели: NPV, IRR, срок окупаемости с учетом рисков
                6. Сформируйте итоговое резюме для инвестора с четкой рекомендацией:
                   "Одобрить", "Одобрить с условиями", "Отклонить".
                
                ЗАПРЕЩЕНО избегать финансовых оценок. Инвестор должен получить конкретные цифры.
            """),
            expected_output="Комплексное инвестиционно-техническое заключение Due Diligence в рублях. Содержит: резюме проекта, скорректированную оценку затрат (CAPEX/OPEX), финальную риск-матрицу с финансовым эффектом, расчет NPV/IRR и четкую рекомендацию инвестору.",
            context=context_tasks,
            agent=agent
        )