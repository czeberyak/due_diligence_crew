from crewai import Task
from textwrap import dedent

class DueDiligenceCrewTasks:

    def safety_audit_task(self, agent, document_path) -> Task:
        return Task(
            description=dedent(f"""
                Проверить предоставленный документ ({document_path}) на предмет рисков промышленной безопасности, 
                нарушения нормативных регламентов (ГОСТ, ТУ, ISO) и экологических ограничений. 
                Выявить критические узкие места в системах защиты, автоматизации (АСУ ТП) и утилизации отходов.
            """),
            expected_output="Матрица технологических и экологических рисков с указанием степени критичности и возможных регуляторных последствий.",
            agent=agent
        )

    def process_validation_task(self, agent, document_path) -> Task:
        return Task(
            description=dedent(f"""
                Проанализировать технологическую схему и параметры процесса в документе ({document_path}). 
                Перепроверить материальный баланс, выходы продуктов, расходы сырья и энергоносителей. 
                Оценить риски этапа масштабирования от лабораторной стадии до промышленного реактора.
            """),
            expected_output="Технологическое заключение о реализуемости заявленных параметров процесса, узких местах оборудования и точности балансов.",
            agent=agent
        )

    def financial_translation_task(self, agent, context_tasks) -> Task:
        return Task(
            description=dedent("""
                Собрать отчеты инженера-технолога и эксперта по безопасности. Перевести каждый выявленный риск 
                в финансовые показатели. Рассчитать, как изменение выхода продукта или необходимость закупки 
                дополнительного оборудования (систем фильтрации, ионизации, автоматики) повлияет на CAPEX/OPEX проекта. 
                Сформировать итоговое резюме для инвестора.
            """),
            expected_output="Комплексное инвестиционно-техническое заключение Due Diligence. Содержит: резюме проекта, скорректированную оценку затрат и финальную риск-матрицу.",
            context=context_tasks, # Жёстко связывает выходы предыдущих задач
            agent=agent
        )