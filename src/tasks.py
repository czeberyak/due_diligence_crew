# src/tasks.py
from crewai import Task
from textwrap import dedent

class DueDiligenceCrewTasks:
    def safety_audit_task(self, agent, document_path) -> Task:
        return Task(
            description=dedent(f"""
                КРИТИЧЕСКИ ВАЖНО: Вы ОБЯЗАНЫ использовать инструменты `regulatory_search` и `semantic_search`.
                1. Прочитайте документ ({document_path}) через `document_search`.
                2. Определите тип проекта (химия, энергетика/СЭС/ВЭС, строительство и т.д.).
                3. На основе типа проекта выберите РЕЛЕВАНТНЫЕ категории нормативной базы.
                4. ИСПОЛЬЗУЙТЕ `regulatory_search` с параметром category.
                5. НЕ ПРОСТО перечисляйте законы — НАЙДИТЕ конкретные пункты и сверьте с ТЭО.
                6. Выявите, чего в ТЭО НЕ ХВАТАЕТ согласно найденным законам.
                ЗАПРЕЩЕНО писать "Полное соответствие" без цитирования конкретных пунктов законов.
            """),
            expected_output="Матрица рисков с выдержками из реальных нормативных документов.",
            agent=agent
        )

    def process_validation_task(self, agent, document_path) -> Task:
        return Task(
            description=dedent(f"""
                КРИТИЧЕСКИ ВАЖНО: Вы ОБЯЗАНЫ использовать инструменты для проверки баланса.
                1. Используя `document_search`, найдите в {document_path} все таблицы баланса.
                2. Определите тип проекта:
                   - ХИМИЧЕСКОЕ: передайте данные в `process_calculator`.
                   - ЭНЕРГЕТИЧЕСКОЕ (СЭС, ВЭС, ТЭЦ): передайте данные в `energy_balance_calculator`.
                3. Если невязка > 5% ИЛИ КИУМ превышает физический максимум — укажите это как КРИТИЧЕСКУЮ ОШИБКУ ФИЗИКИ.
            """),
            expected_output="Точный расчет баланса с вердиктом о реализуемости.",
            agent=agent
        )

    def financial_translation_task(self, agent, context_tasks) -> Task:
        return Task(
            description=dedent("""
                КРИТИЧЕСКИЕ ПРАВИЛА МАТЕМАТИКИ И ФИНАНСОВОГО АНАЛИЗА:
                
                ═══════════════════════════════════════════════════════
                ПРАВИЛО №1: ПРОВЕРКА АРИФМЕТИКИ ВЫРАБОТКИ
                ═══════════════════════════════════════════════════════
                ПЕРЕД записью формулы выработки — ОБЯЗАТЕЛЬНО пересчитайте её вручную:
                Выработка (МВт·ч) = Мощность (МВт) × 8760 ч × КИУМ
                Пример для 50 МВт и КИУМ 20%: 50 × 8760 × 0,20 = 87 600 МВт·ч (НЕ 109 500!)
                ЗАПРЕЩЕНО использовать цифры, которые не сходятся по формуле!
                
                ═══════════════════════════════════════════════════════
                ПРАВИЛО №2: УЧЁТ НАЛОГА НА ПРИБЫЛЬ (20%)
                ═══════════════════════════════════════════════════════
                Чистый денежный поток (CF) ОБЯЗАН учитывать налог на прибыль:
                EBITDA = Доход - OPEX
                Амортизация = CAPEX / Срок_службы (обычно 15-20 лет)
                Налоговая база = max(0, (EBITDA - Амортизация))  # Если убыток, налог = 0
                Налог = Налоговая_база × 20%
                CF после налога = EBITDA - Налог
                ЗАПРЕЩЕНО считать CF = Доход - OPEX без вычета налога!
                
                ═══════════════════════════════════════════════════════
                ПРАВИЛО №3: РЕАЛИСТИЧНЫЕ ОРИЕНТИРЫ ДЛЯ ЭНЕРГЕТИКИ РФ
                ═══════════════════════════════════════════════════════
                • СЭС: КИУМ 14-17%, CAPEX 50-70 тыс ₽/кВт, OPEX 1-2%, IRR 10-14%, Payback 7-10 лет
                • ВЭС: КИУМ 25-35%, CAPEX 90-120 тыс ₽/кВт
                КРИТИЧЕСКАЯ ПРОВЕРКА IRR: Если IRR > 20% для энергопроекта — это СИГНАЛ ОБ ОШИБКЕ!
                
                ═══════════════════════════════════════════════════════
                ПРАВИЛО №4: SENSITIVITY ANALYSIS (АНАЛИЗ ЧУВСТВИТЕЛЬНОСТИ)
                ═══════════════════════════════════════════════════════
                ОБЯЗАТЕЛЬНО рассчитайте и выведите таблицу для трёх сценариев: 
                1. Пессимистичный (Оптовый рынок РСВ без субсидий, тариф 3-4 ₽/кВт·ч)
                2. Базовый (Реалистичные параметры с учётом выявленных рисков)
                3. Оптимистичный (ДПМ ВИЭ с господдержкой, тариф 12-15 ₽/кВт·ч)
                Для каждого сценария укажите NPV, IRR и Payback.
                
                ═══════════════════════════════════════════════════════
                ПРАВИЛО №5: ПЕРЕВОД РИСКОВ В РУБЛИ
                ═══════════════════════════════════════════════════════
                ЗАПРЕЩЕНО использовать "среднюю температуру" (0,5% от CAPEX на каждый риск).
                Используйте реалистичные рыночные оценки (например, подключение к 110 кВ = 80-200 млн ₽, 
                АСКУЭ и SCADA = 15-30 млн ₽, молниезащита = 10-20 млн ₽).
                
                ═══════════════════════════════════════════════════════
                ПРАВИЛО №6: ВЕРДИКТ
                ═══════════════════════════════════════════════════════
                ЗАПРЕЩЕНО писать "Одобрить" при IRR ≈ 41% или Payback ≈ 2,5 года для СЭС — это ГРУБАЯ ОШИБКА!
                Вердикт должен строго соответствовать математике: NPV < 0 или IRR < WACC -> "Отклонить".
                            
                ═══════════════════════════════════════════════════════
                ПРАВИЛО №7: РАЗЛИЧЕНИЕ ТАРИФОВ И МЕХАНИЗМОВ РЫНКА
                ═══════════════════════════════════════════════════════
                • ДПМ ВИЭ (господдержка): 10-15 ₽/кВт·ч, срок 15 лет, требует отбора на конкурсе.
                • Оптовый рынок (РСВ): 2-4 ₽/кВт·ч, без субсидий (проект обычно убыточен при высоких CAPEX).
                • Зелёные сертификаты: дополнительный доход.
                ЗАПРЕЩЕНО называть оптовый тариф РСВ термином "ДПМ ВИЭ"!
            """),
            expected_output=dedent("""
                Комплексное инвестиционно-техническое заключение Due Diligence в формате Markdown.
                ОБЯЗАТЕЛЬНЫЕ разделы:
                1. Проверка арифметики (с ручным пересчетом выработки).
                2. Финансовая модель с учетом налога на прибыль 20%.
                3. Таблица Sensitivity Analysis (3 сценария: РСВ, Базовый, ДПМ ВИЭ).
                4. Оценка рисков в абсолютных значениях (млн ₽) с рыночным обоснованием.
                5. Итоговый вердикт и рекомендация для инвестора.
            """),
            context=context_tasks,
            agent=agent
        )